require('dotenv').config();
const sqlite3 = require('sqlite3').verbose();

class SqliteDbAdapter {
  constructor(dbPath) {
    this.dbPath = dbPath || process.env.DB_PATH;
    this.db = null;
    this.timeout = parseInt(process.env.SQLITE_TIMEOUT) || 5000;
    this.busyTimeout = parseInt(process.env.SQLITE_BUSY_TIMEOUT) || 5000;
  }

  connect() {
    return new Promise((resolve, reject) => {
      this.db = new sqlite3.Database(this.dbPath, (err) => {
        if (err) {
          reject(err);
        } else {
          // Configure database settings
          this.db.configure('busyTimeout', this.busyTimeout);
          if (process.env.DEBUG === 'true') {
            this.db.on('trace', (sql) => console.log('SQL:', sql));
          }
          resolve();
        }
      });
    });
  }

  close() {
    return new Promise((resolve, reject) => {
      if (this.db) {
        this.db.close((err) => {
          if (err) {
            reject(err);
          } else {
            resolve();
          }
        });
      } else {
        resolve();
      }
    });
  }

  run(sql, params = []) {
    return new Promise((resolve, reject) => {
      this.db.run(sql, params, function (err) {
        if (err) {
          reject(err);
        } else {
          resolve({ id: this.lastID, changes: this.changes });
        }
      });
    });
  }

  get(sql, params = []) {
    return new Promise((resolve, reject) => {
      this.db.get(sql, params, (err, row) => {
        if (err) {
          reject(err);
        } else {
          resolve(row);
        }
      });
    });
  }

  all(sql, params = []) {
    return new Promise((resolve, reject) => {
      this.db.all(sql, params, (err, rows) => {
        if (err) {
          reject(err);
        } else {
          resolve(rows);
        }
      });
    });
  }

  exec(sql) {
    return new Promise((resolve, reject) => {
      this.db.exec(sql, (err) => {
        if (err) {
          reject(err);
        } else {
          resolve();
        }
      });
    });
  }
}

module.exports = SqliteDbAdapter;
